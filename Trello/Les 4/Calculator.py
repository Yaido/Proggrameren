eerste_getal = 4                   
operator = "*"                
tweede_getal = 8                  
     

                        
if operator == "+":           
    result = eerste_getal+tweede_getal        
elif operator == "-":         
    result = eerste_getal-tweede_getal        
elif operator == "/":         
    result = eerste_getal/tweede_getal        
elif operator == "*":         
    result = eerste_getal*tweede_getal        
elif operator == "%":         
    result = eerste_getal%tweede_getal        
else:                   
    result = 0          

print(result)           